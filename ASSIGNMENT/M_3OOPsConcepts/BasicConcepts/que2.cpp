//What is OOP? List OOP concepts

/*Object-Oriented Programming (OOP) is a programming paradigm that organizes code into objects, which are instances of classes.*/

// OOP Concepts

/*
1. Classes: Blueprints for creating objects. They define the properties (attributes) and behaviors (methods) of objects.
2. Objects: Instances of classes. They have their own state (attributes) and behavior (methods).
3. Encapsulation: Hiding the internal details of an object and providing public interfaces to interact with it.
4. Inheritance: A class can inherit properties and methods from another class, forming a hierarchical relationship.
5. Polymorphism: Ability of an object to take on many forms. It allows methods to behave differently based on the object's type.
6. Abstraction: Simplifying complex systems by hiding unnecessary details and focusing on the essential aspects.
7. Composition: A class can be composed of other classes, allowing for modular and reusable code.
8. Association: A relationship between two classes where one class uses the other class as a member variable.
9. Aggregation: A special type of association where the lifetime of the associated object is independent of the owner object.
10. Composition: A special type of association where the lifetime of the associated object is dependent on the owner object.
*/